          <h4 class="font-weight-bold">1. Hijab pashmina</h4>
          <h5>Pertama kali populer di tahun 2010, model hijab pashmina diprediksi akan kembali digemari oleh para hijabers. 
            Hijab pashmina 2022 akan hadir dengan pilihan warna yang lebih cerah dengan menonjolkan sentuhan yang feminim. 
            Selain warna pashmina, pashmina instan dengan bahan yang nyaman juga mulai digemari oleh para hijabers.</h5><br>
          <h4 class="font-weight-bold">2. Hijab segi empat</h4>
          <h5>Karena masa pandemi yang mengharuskan masyarakat lebih banyak di rumah membuat model hijab yang praktis lebih 
            banyak diminati. Oleh karena itu, selain pashmina, hijab segi empat juga akan menjadi populer di tahun 2021. 
            Jilbab segiempat bahan katun voal diprediksi akan ramai diburu hijabers karena memang mudah dibentuk dan nyaman 
            digunakan. Bahkan hijab segiempat 2022 akan hadir dengan warna soft, natural, cutting lose dan motif-motif pilihan 
            yang cantik dan minimalis untuk dikenakan di segala suasana.</h5><br>
          <h4 class="font-weight-bold">3. Busana puff sleeves</h4>
          <img src="images/Busana-puff-sleeves.jpg">
          <h5>Trend busana hijab 2022 berikutnya adalah busana puff sleeves. Sesuai dengan namanya, busana puff sleeves ini 
            memiliki detail lengan yang menggembung pada kanan kirinya. Meski bukan fashion baru, namun puff sleeves menjadi 
            salah satu fashion item yang belakangan ini banyak digandrungi oleh para hijabers. Busana puff sleeves ini mampu 
            memberikan nuansa yang chic dan feminim pada penampilan kamu. Bahkan model busana satu ini bisa dengan mudah 
            dipadukan dengan berbagai fashion hijab lainnya.</h5><br>
          <h4 class="font-weight-bold">4. Vest Korea</h4>
          <img src="images/Vest-Korea.jpg">
          <h5>Terinspirasi dari trend fashion Korea, Vest Korea menjadi salah satu trend hijab 2021 di Indonesia. Vest Korea 
            sendiri berbentuk seperti tanktop atau busana tanpa lengan yang dijadikan luaran untuk mempermanis outfit. 
            Item fashion ini bahkan sudah banyak digunakan oleh selebgram hijab tak hanya di Indonesia tapi juga di luar negeri. 
            Penggunaan vest Korea ini memang mampu membuat tampilan semakin modis. Cara padupadannyapun sangat mudah, kamu bisa 
            mengombinasikan vest Korea dengan dress, blus hingga kemeja sesuai style fashion yang kamu inginkan.</h5><br>
          <h4 class="font-weight-bold">5. Sleepwear Piyama</h4>
          <img src="images/Sleepwear-Piyama.jpg">
