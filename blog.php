            cerah ini akan memberikan kesegaran pada penampilan kamu dengan perpaduan warna yang memiliki energi kegembiraan, seperti 
            turquoise, kuning, hingga pink dengan nuansa yang soft.</h5><br>
          <h4 class="font-weight-bold">8. Busana warna lilac</h4>
          <img src="images/Busana-warna-lilac.jpg">
