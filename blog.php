            Warna lilac biasanya identik dengan tampilan yang feminin dan juga berkaratker dan memberiikan kesan estetik hingga 
            mempermanis outfit. Tak hanya warna lilac, lavender dan moss juga diprediksi akan mendominasi trend fashion muslim 2021.</h5><br>
          <h4 class="font-weight-bold">9. Busana muslim bernuansa earthy tone</h4>
          <img src="images/Busana-muslim-bernuansa-earthy-tone.jpg">
          <h5>Meski warna vibrant dan lilac mendominasi 2021, bukan berarti nuansa earthy tone tak lagi diminati. Justru busana muslim 
            dengan nuansa earthy tone akan meramaikan model hijab syari. Berbagai model busana muslim dari hijab hingga gamis akan hadir 
            dengan beragam warna bernuansa earthy tone seperti warna terracotta, kuning, biru muda, dan olive. Warna-warna bernuansa earthy 
            tone akan memberikan nuansa calm dan elegan pada penampilan kamu.</h5><br>
          <h4 class="font-weight-bold">10. Hijab berbahan katun dan voal</h4>
          <h5>Sementara untuk bahan hijab, katun tetap jadi andalan karena tingkat kenyamanannya. Mengingat masih pandemi dan kebanyakan 
            wanita muslim mencari busana yang nyaman dan praktis, jadi nggak heran jika hijab berbahan katun menjadi trend di tahun 2021.
            Selain katun, hijab voal juga menjadi salah satu bahan hijab yang digemari belakangan ini. Bahan hijab voal adalah kain voille 
            yang memiliki beragam warna dan motif. Sifatnya semi transparan, tipis tetapi jika dilipat tidak akan menerawang. Kain voille 
            ini kebanyakan terbuat dari 100% katun. Akan tetapi, ada juga variasi kain voille yang terbuat dari kombinasi serat rayon, linen,
             silk, dan polyester.</h5><br>
