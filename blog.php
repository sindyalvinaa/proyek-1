            diprediksi akan banyak diburu oleh para hijabers. Dengan garis potongan yang sederhana dan rapi serta pengaplikasian warna-warna 
            monochrome, kamu akan tampil lebih fresh dan lebih kekinian.</h5><br>
          <h4 class="font-weight-bold">14. Aksesoris yang minim namun playful</h4>
          <img src="images/Aksesoris-yang-playful.jpg">
